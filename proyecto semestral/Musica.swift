import Foundation

class Musica{
    var titulo: String
    var genero: String
    var artista: String
    var album: String
    var duracion: String
    var imagen: String
    
    
    init(titulo:String, genero:String, artista:String, album:String, duracion:String, imagen:String){
        self.titulo = titulo
        self.genero = genero
        self.artista = artista
        self.album = album
        self.duracion = duracion
        self.imagen = imagen
        
    }
}



